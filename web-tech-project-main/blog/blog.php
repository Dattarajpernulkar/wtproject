                    As the path from producer to market became increasingly complicated, many foods were affected by
                    adulteration and the addition of additives which ranged from relatively harmless ingredients,
                    such as cheap cornstarch and corn syrup, to poisonous ones. Some manufacturers produced bright
                    colors in candy by the addition of hazardous substances for which there was no legal regulation:
                    green (chromium oxide and copper acetate), red (lead oxide and mercury sulfide), yellow (lead
                    chromate) and white (chalk, arsenic trioxide).[16]

                    In an 1885 cover cartoon for Puck, Joseph Keppler satirized the dangers of additives in candy by
                    depicting the "mutual friendship" between striped candy, doctors, and gravediggers. By 1906,
                    research into the dangers of additives, exposés of the food industry, and public pressure led to
                    the passage of the Pure Food and Drug Act, the first federal United States law to regulate food
                    and drugs, including candy.[16]</p>

